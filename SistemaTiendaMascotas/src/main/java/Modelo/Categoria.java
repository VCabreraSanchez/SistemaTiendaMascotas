
package Modelo;

public class Categoria extends Producto{
    private String marca;
    private String Animal;
    private String Tproducto;//tipo de producto


    /**
     * @return the marca
     */
    public String getMarca() {
        return marca;
    }

    /**
     * @param marca the marca to set
     */
    public void setMarca(String marca) {
        this.marca = marca;
    }

    /**
     * @return the Animal
     */
    public String getAnimal() {
        return Animal;
    }

    /**
     * @param Animal the Animal to set
     */
    public void setAnimal(String Animal) {
        this.Animal = Animal;
    }

    /**
     * @return the Tproducto
     */
    public String getTproducto() {
        return Tproducto;
    }

    /**
     * @param Tproducto the Tproducto to set
     */
    public void setTproducto(String Tproducto) {
        this.Tproducto = Tproducto;
    }
    
    

    
}
