    private void btnCambiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCambiarActionPerformed
        if(cmbAnimal.getSelectedIndex() != 0){
            p.setNombP(txtCambNombre.getText());
            p.setPrecioU(Double.parseDouble(txtCambPrecio.getText()));
            p.setStock(Integer.parseInt(txtCambStock.getText()));
            p.setAnimal(cmbAnimal.getSelectedItem().toString());
            obj.CambiarProd(p);
            Cam.setCemp(txtCodEmpleado.getText());
            Cam.setCpro(txtCodProd.getText());
            obj.adiCamb(Cam);
        }
    }//GEN-LAST:event_btnCambiarActionPerformed

