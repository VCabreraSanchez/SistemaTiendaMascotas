    private void tablaManProMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaManProMouseClicked
        int fila = tablaManPro.getSelectedRow();
        p.setCode(tablaManPro.getValueAt(fila, 0).toString());
        p.setNombP(tablaManPro.getValueAt(fila, 1).toString());
        p.setPrecioU(Double.parseDouble(tablaManPro.getValueAt(fila, 2).toString()));
        p.setStock(Integer.parseInt(tablaManPro.getValueAt(fila, 3).toString()));
        p.setAnimal(tablaManPro.getValueAt(fila, 4).toString());
        txtCodProd.setText(tablaManPro.getValueAt(fila, 0).toString());
        activarCasillas();
    }//GEN-LAST:event_tablaManProMouseClicked

