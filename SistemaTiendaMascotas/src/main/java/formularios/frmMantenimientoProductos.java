    void filtraNombre(String cad){
        DefaultTableModel dt = (DefaultTableModel)tablaManPro.getModel();
        dt.setRowCount(0);
        obj.lisPro().stream()
                .filter(x->x.getNombP().toLowerCase().startsWith(cad.toLowerCase()))
                .forEach(x->dt.addRow(new Object[]{x.getCode(),x.getNombP(),x.getPrecioU(),x.getStock(),x.getAnimal()}));
    }
    
