
package Modelo;

public class TipoHigiene extends Producto{
    private String Hcode;
    private String Hnombre;

    /**
     * @return the Hcode
     */
    public String getHcode() {
        return Hcode;
    }

    /**
     * @param Hcode the Hcode to set
     */
    public void setHcode(String Hcode) {
        this.Hcode = Hcode;
    }

    /**
     * @return the Hnombre
     */
    public String getHnombre() {
        return Hnombre;
    }

    /**
     * @param Hnombre the Hnombre to set
     */
    public void setHnombre(String Hnombre) {
        this.Hnombre = Hnombre;
    }
    
    
}
