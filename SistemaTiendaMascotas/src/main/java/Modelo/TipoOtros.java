
package Modelo;

public class TipoOtros extends Producto {
    private String Ocode;
    private String Onombre;

    /**
     * @return the Ocode
     */
    public String getOcode() {
        return Ocode;
    }

    /**
     * @param Ocode the Ocode to set
     */
    public void setOcode(String Ocode) {
        this.Ocode = Ocode;
    }

    /**
     * @return the Onombre
     */
    public String getOnombre() {
        return Onombre;
    }

    /**
     * @param Onombre the Onombre to set
     */
    public void setOnombre(String Onombre) {
        this.Onombre = Onombre;
    }
    
    
}
